     private void loadData() {
        modelMahasiswa.setRowCount(0);
        int no = 1;

    try {
        Class.forName(driver);
        conn = DriverManager.getConnection(koneksi, user, password);

        String sql = "SELECT nim, nama, tahunmasuk, jenismahasiswa, jumlahsks, biayakuliah FROM public.\"Mahasiswa\"";
        stmt = conn.createStatement();
        ResultSet rs = stmt.executeQuery(sql);

        DecimalFormatSymbols symbols = new DecimalFormatSymbols(new Locale("id","ID"));
        symbols.setGroupingSeparator('.');
        symbols.setDecimalSeparator(',');

        DecimalFormat df = new DecimalFormat("#,##0.00", symbols);

        // ✅ HANYA SATU while
        while (rs.next()) {
            Object[] data = {
                rs.getString("nim"),
                rs.getString("nama"),
                rs.getInt("tahunmasuk"),
                rs.getString("jenismahasiswa"),
                rs.getInt("jumlahsks"),
                df.format(rs.getDouble("biayakuliah")) // 2 desimal
            };
            modelMahasiswa.addRow(data);
        }

        rs.close();
        stmt.close();
        conn.close();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal load data: " + e.getMessage());
    }
    }
