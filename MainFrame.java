    private void btnUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadActionPerformed
                                    
    JFileChooser chooser = new JFileChooser();
    if (chooser.showOpenDialog(this) != JFileChooser.APPROVE_OPTION) {
        return;
    }

    File file = chooser.getSelectedFile();

    try (
        BufferedReader br = new BufferedReader(new FileReader(file))
    ) {
        Class.forName(driver);
        conn = DriverManager.getConnection(koneksi, user, password);

        String line;
        while ((line = br.readLine()) != null) {

            // Skip baris kosong
            if (line.trim().isEmpty()) continue;

            String[] d = line.split(",");

            // Validasi jumlah kolom CSV
            if (d.length != 5) {
                JOptionPane.showMessageDialog(
                    this,
                    "Format CSV salah!\nGunakan: nim,nama,tahunmasuk,jenismahasiswa,jumlahsks"
                );
                return;
            }

            String nim = d[0].trim();
            String nama = d[1].trim();
            int tahunmasuk = Integer.parseInt(d[2].trim());
            String jenis = d[3].trim();
            int jumlahsks = Integer.parseInt(d[4].trim());

            // =======================
            // HITUNG BIAYA (OOP)
            // =======================
            Mahasiswa m;
            if (jenis.equalsIgnoreCase("Reguler")) {
                m = new Reguler(0, nim, nama, tahunmasuk, jumlahsks);
            } else if (jenis.equalsIgnoreCase("Beasiswa")) {
                m = new Beasiswa(0, nim, nama, tahunmasuk, jumlahsks);
            } else {
                m = new Internasional(0, nim, nama, tahunmasuk, jumlahsks);
            }

            m.hitungBiaya();
            double biayakuliah = m.getbiayakuliah();

            // =======================
            // INSERT DATABASE
            // =======================
            String sql = "INSERT INTO public.\"Mahasiswa\" "
                       + "(nim, nama, tahunmasuk, jenismahasiswa, jumlahsks, biayakuliah) "
                       + "VALUES (?,?,?,?,?,?)";

            pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, nim);
            pstmt.setString(2, nama);
            pstmt.setInt(3, tahunmasuk);
            pstmt.setString(4, jenis);
            pstmt.setInt(5, jumlahsks);
            pstmt.setDouble(6, biayakuliah);
            pstmt.executeUpdate();
        }

        JOptionPane.showMessageDialog(this, "Upload CSV berhasil!");
        loadData();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error upload: " + e.getMessage());
    } finally {
        try {
            if (pstmt != null) pstmt.close();
            if (conn != null) conn.close();
        } catch (Exception ex) {
            // abaikan
        }
    }
    }//GEN-LAST:event_btnUploadActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int row = tblMahasiswa.getSelectedRow();
        if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data yang ingin diedit!");
        return;
        }

        String nim = modelMahasiswa.getValueAt(row, 0).toString();
        String nama = txtNama.getText().trim();
        String sksText = txtJumlahSks.getText().trim();
        String jenis = cmbJenisMahasiswa.getSelectedItem().toString();

        if (nama.isEmpty() || sksText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Nama dan SKS tidak boleh kosong!");
        return;
        }

        int sks;
        try {
            sks = Integer.parseInt(sksText);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "SKS harus berupa angka!");
            return;
        }

    try {
        Class.forName(driver);
        conn = DriverManager.getConnection(koneksi, user, password);

        String sql = "UPDATE public.\"Mahasiswa\" "
                   + "SET nama=?, jenismahasiswa=?, jumlahsks=? "
                   + "WHERE nim=?";

        pstmt = conn.prepareStatement(sql);
        pstmt.setString(1, nama);
        pstmt.setString(2, jenis);
        pstmt.setInt(3, sks);
        pstmt.setString(4, nim);
        pstmt.executeUpdate();

        JOptionPane.showMessageDialog(this, "Data berhasil diupdate!");
        loadData();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_btnEditActionPerformed

